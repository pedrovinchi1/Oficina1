    private void submitButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitButtonMouseClicked
        String total = "Erro inesperado";
        String nom = "Nome não encontrado";
        try{
            int RA = Integer.parseInt(RAMembro.getText());
            String sen = senha.getText();    
            //SQL Horas
            Statement statement1 = conn.createStatement();
            ResultSet resultSet1 = statement1.executeQuery("SELECT SUM(duracao) AS total FROM atividade WHERE MEMBRO_RA LIKE '"+RA+"'");
            while (resultSet1.next()) {
                total = resultSet1.getString("total");
            }
            numHoras.setText(total);
            //SQL Nome
            Statement statement2 = conn.createStatement();
            ResultSet resultSet2 = statement2.executeQuery("SELECT nomeMembro AS nome from membro where RA like "+RA+" AND senha Like '"+sen+"'");
            while (resultSet2.next()) {
                nom = resultSet2.getString("nome");
            }
            nomMembro.setText(nom);
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Ocorreu um erro inesperado!!!");
        }catch(NumberFormatException n){
            JOptionPane.showMessageDialog(null, "O formato dos campos é inválido!!!");
        }
        
    }//GEN-LAST:event_submitButtonMouseClicked


